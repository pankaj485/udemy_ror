# with string, if used single quote we can't use string interpolation
# in terminal, type command `irb` to get the interactive ruby terminal
# use `#{<variable_name>}` to use string interpolation in ruby
# If one variable is pointing to another variable then it is actually pointing to the memory location of that variable.
# use backslash `\` to skip a character.

my_name = "pankaj"

puts "my name is \"#{my_name}\" "
